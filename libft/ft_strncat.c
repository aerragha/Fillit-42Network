#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	char	*s;
	size_t	l;

	i = 0;
	s = (char*)s2;
	l = ft_strlen(s1);
	while (s[i] != '\0' && i < n)
	{
		s1[l] = s[i];
		l++;
		i++;
	}
	s1[l] = '\0';
	return (s1);
}
