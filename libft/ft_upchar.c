#include "libft.h"

char	*ft_upchar(char *s)
{
	int i;

	i = 1;
	if (s[0] >= 'a' && s[0] <= 'z')
		s[0] = s[0] - 'a' + 'A';
	while (s[i] != '\0')
	{
		if (s[i] >= 'A' && s[i] <= 'Z')
			s[i] = s[i] - 'A' + 'a';
		i++;
	}
	return (s);
}
