#include "libft.h"

int	ft_islower(char *s)
{
	int i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] >= 'A' && s[i] <= 'Z')
			return (0);
		i++;
	}
	return (1);
}
