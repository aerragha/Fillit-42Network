#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	size_t	l;
	char	*s;

	i = 0;
	s = (char*)s2;
	l = ft_strlen(s1);
	while (s[i] != '\0')
	{
		s1[l] = s[i];
		l++;
		i++;
	}
	s1[l] = '\0';
	return (s1);
}
